
public class Ser{

        String nombre , especie , sexo , estado ;
        public Ser (String nombre , String especie, String sexo, String estado){
		
		this.nombre = nombre;
		this.especie = especie;
		this.sexo = sexo;
		this.estado = estado;
		}
		
	public String getNombre(){
	return nombre;
	}
	
	public String getEspecie(){
	return especie;
	}
	
	public String getSexo(){
	return sexo;
	}
	
	public String getEstado(){
	return estado;
	}
	
}